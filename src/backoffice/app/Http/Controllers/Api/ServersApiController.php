        $servers_url = Setting::where('name', TypeInterface::Types_Setting_TruckersMP_Server)
            ->firstOrFail();

        $servers_url = is_null($servers_url->value) ? $servers_url->default : $servers_url->value;

        $contents = file_get_contents($servers_url);

        if(!is_null($contents)){
            $contents = json_decode($contents);
            return response()->json($contents);
        }
    }

